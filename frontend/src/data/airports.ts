// Function to get airports with local storage caching
export function getAirports(): Airport[] {
    const cachedData = localStorage.getItem('airports');
    if (cachedData) {
        const { version, data } = JSON.parse(cachedData);
        if (version === AIRPORTS_VERSION) {
            return data;
        }
    }

    // If no cache or outdated version, store the new data
    localStorage.setItem('airports', JSON.stringify({
        version: AIRPORTS_VERSION,
        data: AIRPORTS
    }));

    return AIRPORTS;
} 